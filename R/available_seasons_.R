#' Print available football seasons
#'
#' This function prints the available football seasons for which statistics
#' are available.
#'
#' @return Prints the available football seasons to the console.
#'
#' @examples
#'
#' # Print available football seasons
#' available_seasons()
#'
available_seasons <- function() {
  cat("Available football seasons:\n")
  cat("  - 2018/2019\n")
  cat("  - 2019/2020\n")
  cat("  - 2020/2021\n")
  cat("  - 2021/2022\n")
  cat("  - 2022/2023\n")
  cat("  - 2023/2024\n")
}
